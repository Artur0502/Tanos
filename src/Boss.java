public class Boss {
    private int damage;

    private String tyreDefence;

    private int xp;

    public int getDamage() {
        return damage;
    }

    public void setDamage(int damage) {
        this.damage = damage;
    }

    public String getTyreDefence() {
        return tyreDefence;
    }

    public void setTyreDefence(String tyreDefence) {
        this.tyreDefence = tyreDefence;
    }

    public int getXp() {
        return xp;
    }

    public void setXp(int xp) {
        this.xp = xp;
    }
}

